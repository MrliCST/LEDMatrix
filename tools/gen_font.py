# Generate Adafruit GFXfont header with ROW-MAJOR bitmap packing

def encode_glyph(rows):
    """rows: list of 7 strings, each 5 chars. Returns list of int bytes (row-major, MSB-first)."""
    bits = []
    for row in rows:
        for ch in row:
            bits.append(1 if ch == '#' else 0)
    while len(bits) % 8 != 0:
        bits.append(0)
    result = []
    for i in range(0, len(bits), 8):
        byte = 0
        for j in range(8):
            byte = (byte << 1) | bits[i + j]
        result.append(byte)
    return result

