lines.append('#define MYFONT_H')
lines.append('')

lines.append('uint8_t MyFontBitmaps[] PROGMEM = {')
for i in range(0, len(bitmap_bytes), 12):
    chunk = bitmap_bytes[i:i+12]
    hexes = ', '.join(f'0x{b:02X}' for b in chunk)
    if i + 12 < len(bitmap_bytes):
        hexes += ','
    lines.append(f'    {hexes}')
lines.append('};')
lines.append('')

lines.append('GFXglyph MyFontGlyphs[] PROGMEM = {')
for (offset, w, h, adv, xoff, yoff) in glyphs:
    lines.append(f'    {{ {offset:4d}, {w}, {h}, {adv}, {xoff}, {yoff} }},')
lines.append('};')
lines.append('')

lines.append('GFXfont MyFont PROGMEM = {')
