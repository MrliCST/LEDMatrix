bitmap_bytes = []
glyphs = []

for code in range(0x20, 0x7F):
    rows = chars[code]
    data = encode_glyph(rows)
    bitmapOffset = len(bitmap_bytes)
    bitmap_bytes.extend(data)

    width = 5
    for w in range(5, 0, -1):
        has = any(row[w-1] == '#' for row in rows)
        if has:
            width = w
            break
    else:
        width = 3

    xAdvance = width + 1
    glyphs.append((bitmapOffset, width, 7, xAdvance, 0, 0))

